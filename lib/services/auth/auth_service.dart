import 'package:firebase_core/firebase_core.dart';
import 'package:notes_app/services/auth/auth_provider.dart';
import 'package:notes_app/services/auth/auth_user.dart';
import 'package:notes_app/services/auth/firebase_auth_provider.dart';

import '../../firebase_options.dart';

//EXPOSES THE FUNCTIONALITY OF FIREBASEAUTHPROVIDER TO UI,
//IN ORDER TO USE IT WE NEED A PROVIDE
class AuthService implements AuthProvider {
  final AuthProvider provider;
  const AuthService(this.provider);

  //THIS SERVICE IS ALREADY CONFIGURED WITH FIREBASEAUTHPROVIDER
  factory AuthService.firebase() => AuthService(FirebaseAuthProvider());

  @override
  Future<AuthUser> createUser({
    required String email,
    required String password,
  }) =>
      provider.createUser(
        email: email,
        password: password,
      );

  @override
  AuthUser? get currentUser => provider.currentUser;

  @override
  Future<AuthUser> logIn({
    required String email,
    required String password,
  }) =>
      provider.logIn(
        email: email,
        password: password,
      );

  @override
  Future<void> logOut() => provider.logOut();

  @override
  Future<void> sendEmailVerification() => provider.sendEmailVerification();

  @override
  Future<void> initialize() => provider.initialize();

}